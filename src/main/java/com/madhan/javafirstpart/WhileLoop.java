public class WhileLoop {

public static void main(String[] args) {
    //This is loop concept
    int i = 0; // This is initialize the zero.

    while (i < 100) { // This is condition for check the value to less then 100

        i++; // this is increament the value to 100.
        System.out.println("Value : " + i);

    }

}

}
